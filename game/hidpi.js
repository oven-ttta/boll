// ------------------------------------------------------------
// ปัญหาเดิม: กราฟิกทุกชิ้นถูก "อบ" เป็น texture bitmap ที่ความละเอียด
// เท่าพิกัดออกแบบ (1x) แล้วแคนวาส 1280x720 ถูก Scale.FIT ขยายเต็มจอ
// พอจอความละเอียดสูง/ซูม texel เท่าเดิมเลยเบลอและขอบหยัก
//
// วิธีแก้: เรนเดอร์ทุกอย่างที่ความละเอียด SS เท่า (ทั้ง backing store
// ของแคนวาส, ทุก texture และตั้ง camera.zoom = SS) โดยพิกัด "โลก"
// ยังเป็นระบบ 1280x720 เหมือนเดิม โค้ดจึงแทบไม่ต้องแก้ตรรกะ
// รูปที่อบไว้จะถูกแสดงที่สเกล 1/SS เพื่อคงขนาดเดิมแต่มี texel เพิ่มขึ้น
// ============================================================

export const SS = (function () {
  var dpr = 1;
  if (typeof window !== 'undefined' && window.devicePixelRatio) {
    dpr = window.devicePixelRatio;
  }
  // อย่างน้อย 2 เท่า (กันเบลอตอน FIT ขยายบนจอ 1080p ที่ dpr=1)
  // ไม่เกิน 3 เท่า (คุมหน่วยความจำ/ประสิทธิภาพบนจอ dpr สูงมาก)
  return Math.max(2, Math.min(3, Math.ceil(dpr)));
})();
